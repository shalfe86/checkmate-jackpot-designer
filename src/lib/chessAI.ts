function getSquareIndex(square: Square, isBlack: boolean): number {
  const file = square.charCodeAt(0) - 97; // a=0, b=1, etc.
  const rank = parseInt(square[1]) - 1; // 1=0, 2=1, etc.
  const index = (7 - rank) * 8 + file;
  return isBlack ? 63 - index : index;
}

export function evaluateBoard(game: Chess): number {
  const board = game.board();
  let score = 0;

  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (piece) {
        const isBlack = piece.color === 'b';
        const pieceValue = PIECE_VALUES[piece.type] || 0;
        const table = PIECE_TABLES[piece.type] || [];
        const squareIndex = row * 8 + col;
        const tableIndex = isBlack ? 63 - squareIndex : squareIndex;
        const positionBonus = table[tableIndex] || 0;

        if (isBlack) {
          score -= pieceValue + positionBonus;
        } else {
          score += pieceValue + positionBonus;
        }
      }
    }
  }

  return score;
}

// Minimax with Alpha-Beta pruning
function minimax(
  game: Chess,
  depth: number,
  alpha: number,
  beta: number,
  isMaximizing: boolean
): number {
  if (depth === 0 || game.isGameOver()) {
    return evaluateBoard(game);
  }

  const moves = game.moves();

  if (isMaximizing) {
    let maxEval = -Infinity;
    for (const move of moves) {
      game.move(move);
      const evalScore = minimax(game, depth - 1, alpha, beta, false);
      game.undo();
      maxEval = Math.max(maxEval, evalScore);
      alpha = Math.max(alpha, evalScore);
      if (beta <= alpha) break;
    }
    return maxEval;
  } else {
    let minEval = Infinity;
    for (const move of moves) {
      game.move(move);
      const evalScore = minimax(game, depth - 1, alpha, beta, true);
      game.undo();
      minEval = Math.min(minEval, evalScore);
      beta = Math.min(beta, evalScore);
      if (beta <= alpha) break;
    }
    return minEval;
  }
}

export function getBestMove(game: Chess, depth: number = 2): string | null {
  const moves = game.moves();
  if (moves.length === 0) return null;

  let bestMove = moves[0];
  let bestEval = Infinity; // AI plays as black, so minimize

  for (const move of moves) {
    game.move(move);
    const evalScore = minimax(game, depth - 1, -Infinity, Infinity, true);
    game.undo();

    if (evalScore < bestEval) {
      bestEval = evalScore;
      bestMove = move;
    }
  }

  // Add slight randomness for moves with similar evaluations (makes AI less predictable)
  const similarMoves = moves.filter((move) => {
    game.move(move);
    const evalScore = minimax(game, depth - 1, -Infinity, Infinity, true);
    game.undo();
    return Math.abs(evalScore - bestEval) < 50;
  });

  if (similarMoves.length > 1 && Math.random() < 0.3) {
    return similarMoves[Math.floor(Math.random() * similarMoves.length)];
  }

  return bestMove;
}
